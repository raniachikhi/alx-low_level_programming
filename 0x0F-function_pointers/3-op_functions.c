#include "3-calc.h"

/**
 * op_add - function of addition operation
 * @a: first integer
 * @b: second integer
 *
 * Return: output
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function of subtraction operation
 * @a: first integer
 * @b: second integer
 *
 * Return: output
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function of multiplication operation
 * @a: first integer
 * @b: second integer
 *
 * Return: output
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function of division operation
 * @a: first integer
 * @b: second integer
 *
 * Return: output
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - function of modulo operation
 * @a: first integer
 * @b: second integer
 *
 * Return: output
 */

int op_mod(int a, int b)
{
	return (a % b);
}
